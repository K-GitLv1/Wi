public class Array4 {

    
    public static void main(String[] args) {

        int score[] = {500,-124,-122,-142};
        int sum = 0;
        
        System.out.println(" Data ");
        for (int i=0; i<score.length; i++){
            System.out.println("Data = "+score[i]);
            sum += score[i];
        }
        System.out.println("\n"+"Data sum: "+sum+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n"+"\n");
    }
    
}
